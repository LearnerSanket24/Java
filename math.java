public class math {
    public static void main(String[] args){
        System.out.println(Math.PI);
        System.out.println(Math.E);

        double a = Math.pow(2,5);
        double b = Math.abs(-2.78); //mode function
        double c = Math.sqrt(49);
        double d = Math.round(2.67);
        double e = Math.ceil(2.67);
        double f = Math.floor(2.67); // greatest integer function
        double g = Math.max(20,40);
        double h = Math.min(20,80);

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }
}
